import java.util.*;
public class swtich{
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int button = sc.nextInt();
        switch(button){
            case 1: System.out.println("kashish");
            break;
            case 2: System.out.println("Priyanshi");
            break;
            case 3: System.out.println("pihu");
            break;
            case 4: System.out.println("default");
            
        }
    }
}
//In Java, break is used to terminate the current loop or switch statement immediately